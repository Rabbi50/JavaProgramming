
public class Switch {
	public static void main(String[] args){
		
		String name="kabir"; 
		switch(name){
		case "rabbi":
			System.out.println("rabbi is a programmer");
			break;
		case "saikat":
			System.out.println("saikat is a cricketer");
			break;
		case "titas":
			System.out.println("titas is a food lover");
			break;
		case "rasel":
			System.out.println("rasel is a footballer");
			break;
			default:
				System.out.println("Hi");
			
		}
	}

}
